package com.savare.beans;

public class PortadorBancoBeans {

	private int idPortadorBanco,
				codigoPortadorBanco;
	private String dataAlteracao,
				   descricaoPortador,
				   siglaPortador;
	private String digitoPortador,
				 tipo;

	public PortadorBancoBeans() {
		
	}

	/**
	 * @return the idPortadorBanco
	 */
	public int getIdPortadorBanco() {
		return idPortadorBanco;
	}

	/**
	 * @param idPortadorBanco the idPortadorBanco to set
	 */
	public void setIdPortadorBanco(int idPortadorBanco) {
		this.idPortadorBanco = idPortadorBanco;
	}

	/**
	 * @return the codigoPortadorBanco
	 */
	public int getCodigoPortadorBanco() {
		return codigoPortadorBanco;
	}

	/**
	 * @param codigoPortadorBanco the codigoPortadorBanco to set
	 */
	public void setCodigoPortadorBanco(int codigoPortadorBanco) {
		this.codigoPortadorBanco = codigoPortadorBanco;
	}

	/**
	 * @return the dataAlteracao
	 */
	public String getDataAlteracao() {
		return dataAlteracao;
	}

	/**
	 * @param dataAlteracao the dataAlteracao to set
	 */
	public void setDataAlteracao(String dataAlteracao) {
		this.dataAlteracao = dataAlteracao;
	}

	/**
	 * @return the descricaoPortador
	 */
	public String getDescricaoPortador() {
		return descricaoPortador;
	}

	/**
	 * @param descricaoPortador the descricaoPortador to set
	 */
	public void setDescricaoPortador(String descricaoPortador) {
		this.descricaoPortador = descricaoPortador;
	}

	/**
	 * @return the siglaPortador
	 */
	public String getSiglaPortador() {
		return siglaPortador;
	}

	/**
	 * @param siglaPortador the siglaPortador to set
	 */
	public void setSiglaPortador(String siglaPortador) {
		this.siglaPortador = siglaPortador;
	}

	/**
	 * @return the digitoPortador
	 */
	public String getDigitoPortador() {
		return digitoPortador;
	}

	/**
	 * @param digitoPortador the digitoPortador to set
	 */
	public void setDigitoPortador(String digitoPortador) {
		this.digitoPortador = digitoPortador;
	}

	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}

	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	
}
