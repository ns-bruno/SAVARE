package com.savare.beans;

public class ParametrosLocalBeans {

    private Integer idParam;
    private String nomeParam, valorParam;

    public Integer getIdParam() {
        return idParam;
    }

    public void setIdParam(Integer idParam) {
        this.idParam = idParam;
    }

    public String getNomeParam() {
        return nomeParam;
    }

    public void setNomeParam(String nomeParam) {
        this.nomeParam = nomeParam;
    }

    public String getValorParam() {
        return valorParam;
    }

    public void setValorParam(String valorParam) {
        this.valorParam = valorParam;
    }
}
