package com.savare.beans;

public class TipoDocumentoBeans {
	
	private int idTipoDocumento,
				idEmpresa,
				codigoTipoDocumento;
	private String descricaoTipoDocumento,
				   siglaTipoDocumento,
				   dataAlteracao;
	private String tipoVenda;
	
	
	/**
	 * @return the idTipoDocumento
	 */
	public int getIdTipoDocumento() {
		return idTipoDocumento;
	}
	/**
	 * @param idTipoDocumento the idTipoDocumento to set
	 */
	public void setIdTipoDocumento(int idTipoDocumento) {
		this.idTipoDocumento = idTipoDocumento;
	}
	/**
	 * @return the idEmpresa
	 */
	public int getIdEmpresa() {
		return idEmpresa;
	}
	/**
	 * @param idEmpresa the idEmpresa to set
	 */
	public void setIdEmpresa(int idEmpresa) {
		this.idEmpresa = idEmpresa;
	}
	/**
	 * @return the codigoTipoDocumento
	 */
	public int getCodigoTipoDocumento() {
		return codigoTipoDocumento;
	}
	/**
	 * @param codigoTipoDocumento the codigoTipoDocumento to set
	 */
	public void setCodigoTipoDocumento(int codigoTipoDocumento) {
		this.codigoTipoDocumento = codigoTipoDocumento;
	}
	/**
	 * @return the descricaoTipoDocumento
	 */
	public String getDescricaoTipoDocumento() {
		return descricaoTipoDocumento;
	}
	/**
	 * @param descricaoTipoDocumento the descricaoTipoDocumento to set
	 */
	public void setDescricaoTipoDocumento(String descricaoTipoDocumento) {
		this.descricaoTipoDocumento = descricaoTipoDocumento;
	}
	/**
	 * @return the siglaTipoDocumento
	 */
	public String getSiglaTipoDocumento() {
		return siglaTipoDocumento;
	}
	/**
	 * @param siglaTipoDocumento the siglaTipoDocumento to set
	 */
	public void setSiglaTipoDocumento(String siglaTipoDocumento) {
		this.siglaTipoDocumento = siglaTipoDocumento;
	}
	/**
	 * @return the dataAlteracao
	 */
	public String getDataAlteracao() {
		return dataAlteracao;
	}
	/**
	 * @param dataAlteracao the dataAlteracao to set
	 */
	public void setDataAlteracao(String dataAlteracao) {
		this.dataAlteracao = dataAlteracao;
	}
	/**
	 * @return the tipoVenda
	 */
	public String getTipoVenda() {
		return tipoVenda;
	}
	/**
	 * @param tipoVenda the tipoVenda to set
	 */
	public void setTipoVenda(String tipoVenda) {
		this.tipoVenda = tipoVenda;
	}
	
	
	
	
}
